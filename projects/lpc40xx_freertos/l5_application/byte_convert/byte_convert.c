static void byte_convert__private_convert_bytes(uint8_t *bytes, uint64_t source_value, size_t number_of_bytes) {
  for (size_t byte_index = number_of_bytes; byte_index > 0U; byte_index--) {
    bytes[number_of_bytes - byte_index] = (source_value >> ((byte_index - 1U) * 8U)) & 0xFF;
  }
}
/***********************************************************************************************************************
 *
 *                                          P U B L I C   F U N C T I O N S
 *
 **********************************************************************************************************************/
void byte_convert__to_network_from_uint64(uint8_t bytes[8], uint64_t source_value) {
  byte_convert__private_convert_bytes(bytes, source_value, 8U);
}
void byte_convert__to_network_from_uint32(uint8_t bytes[4], uint32_t source_value) {
  byte_convert__private_convert_bytes(bytes, source_value, 4U);
}
void byte_convert__to_network_from_uint16(uint8_t bytes[2], uint16_t source_value) {
  byte_convert__private_convert_bytes(bytes, source_value, 2U);
}